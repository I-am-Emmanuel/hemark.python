print("Compound interest calculator")
initial_money = int(input("Enter your principal: "))
percent = 100
return_rate = float(input("Enter your rate: ")) / percent
years_of_interest = int(input("Enter number of years: "))
amount_on_deposit_at_the_end_of_the_year = initial_money * (1 + return_rate) ** years_of_interest

amount_on_deposit_at_the_end_of_the_year = float(amount_on_deposit_at_the_end_of_the_year)

print("This is the amount you've earned after your deposit: ", amount_on_deposit_at_the_end_of_the_year)


